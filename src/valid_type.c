#include "ft_printf.h"

int		valid_arg(char c)
{
	return (c == 'd' || c == 'D' || c == 'i' || c == 's' || c == 'S' || c == 'p'
	|| c == 'o' || c == 'O' || c == 'x' || c == 'X' || c == 'u' || c == 'U'
	|| c == 'c' || c == 'C' || c == '%');
}

int		valid_length(char c)
{
	return (c == 'h' || c == 'l' || c == 'j' || c == 'z');
}

int		valid_flag(char c)
{
	return (c == '#' || c == '0' || c == '-' || c == '+' || c == ' ');
}
